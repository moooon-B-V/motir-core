// Post-build: rewrite `dist/index.js` as a THIN re-export barrel.
//
// tsup bundles each entry, so the `src/index.ts` entry emits an `index.js` that
// INLINES every re-exported module's code — including the theme provider's
// top-level `import { createContext } from 'react'` — with no `'use client'`
// directive on the barrel. A Next RSC consumer that imports a *server-safe*
// export (e.g. `buildThemeInitScript`) through that barrel then crashes:
// "You're importing a module that depends on `createContext` into a React
// Server Component" (the barrel eagerly pulls a client-only API server-side).
//
// tsup already emits every source module 1:1 as its own chunk (with the
// per-file `'use client'` directive preserved by preserve-use-client.mjs), so
// the fix is to make the barrel a THIN re-export of those sibling chunks. Then
// a server import resolves straight to the server-safe chunk, and each client
// primitive stays behind its own `'use client'` boundary — exactly the RSC
// split the ADR (§5) fixes. This mirrors how RSC-compatible component libraries
// ship a mixed barrel.
//
// It is generated FROM `src/index.ts` (rewriting relative specifiers to `.js`
// and dropping type-only lines, which live in the aggregate `index.d.ts`), so
// the barrel's public surface can never drift from source.

import { readFileSync, writeFileSync, rmSync } from 'node:fs';

const SRC = 'src/index.ts';
const OUT = 'dist/index.js';

const header =
  '// @motir/design-system — thin re-export barrel (generated by\n' +
  '// scripts/build-index-barrel.mjs; see that file for the RSC rationale).\n';

const lines = readFileSync(SRC, 'utf8').split('\n');
const out = [];
for (const line of lines) {
  const t = line.trim();
  // Type-only re-exports are erased at runtime — they belong to index.d.ts only.
  if (/^export\s+type\b/.test(t)) continue;
  // Rewrite a relative module specifier to its emitted `.js` chunk. Only value
  // re-export lines reach here (`export * from '...'` / `export { … } from '...'`).
  const rewritten = line.replace(
    /(from\s+')(\.[^']*?)('\s*;?)/g,
    (_m, pre, spec, post) => `${pre}${spec}${spec.endsWith('.js') ? '' : '.js'}${post}`,
  );
  out.push(rewritten);
}

writeFileSync(OUT, header + out.join('\n'));
// The bundled sourcemap no longer matches the thin barrel — drop the orphan.
try {
  rmSync('dist/index.js.map');
} catch {
  /* no map emitted — fine */
}
console.warn('[build-index-barrel] wrote thin re-export dist/index.js');
